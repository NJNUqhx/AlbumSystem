<template>
    <ContentField>
        <div class="row justify-content-md-center">
            <div class="col-3">
                <form @submit.prevent="register">
                    <div class="mb-3">
                        <label for="account" class="form-label">账号</label>
                        <input v-model="account" type="text" class="form-control" id="account" placeholder="请输入账号">
                    </div>
                    <div class="mb-3">
                        <label for="password" class="form-label">密码</label>
                        <input v-model="password" type="password" class="form-control" id="password" placeholder="请输入密码"
                            autocomplete="off">
                    </div>
                    <div class="mb-3">
                        <label for="confirmedPassword" class="form-label">确认密码</label>
                        <input v-model="confirmedPassword" type="password" class="form-control" id="confirmedPassword"
                            placeholder="请确认密码" autocomplete="off">
                    </div>
                    <div class="error-message">
                        {{ error_message }}
                    </div>
                    <button type="submit" class="btn btn-primary">登录</button>
                </form>
            </div>
        </div>
    </ContentField>
</template>

<script>
import ContentField from '../components/ContentField'
import { ref } from 'vue'
import router from '../router/index'
import $ from 'jquery'

export default {
    name: "RegisterView",
    components: {
        ContentField
    },
    setup() {
        let account = ref('');
        let password = ref('');
        let confirmedPassword = ref('');
        let error_message = ref('');
        const register = () => {
            $.ajax({
                url: "http://127.0.0.1:3000/admin/account/register/",
                type: "post",
                data: {
                    account: account.value,
                    password: password.value,
                    confirmedPassword: confirmedPassword.value
                },
                success(resp) {
                    if (resp.error_message === "success")
                        router.push({ name: "login" });
                    else {
                        error_message.value = resp.error_message;
                    }
                }
            })
        }
        return {
            account,
            password,
            confirmedPassword,
            error_message,
            register
        }
    }
}
</script>

<style scoped>
button {
    width: 100%;

}

div.error-message {
    color: red;
}
</style>